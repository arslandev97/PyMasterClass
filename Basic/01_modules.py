# Python Modules 
# Module is like a code library which can be used to borrow the code written by somebody else in our Python Program. 

# Types of Modules 
# 1. Built in Modules
# 2. External Modules

# 1. Built in Modules
# These Modules are ready to import and use and ship with the python Intrepreter.
# there is no need to install such modules explicitly.


# 2. External Modules
# These Modules are imported from third party file or can be installed using a package manager like pip or anconda. Since this code is written by someone else, we can install different versions of a same module with time.

 


